package chapter1;

public class OperatorDemon1 {
    public static void main(String[] args) {
        int a = 987;
        int ge = a % 10;
        System.out.println(ge);
        int shi = a / 10 % 10;
        System.out.println(shi);
        int bai = a / 100;
        System.out.println(bai);
    }
}
